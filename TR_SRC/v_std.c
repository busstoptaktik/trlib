#include <math.h>

#ifndef    M_PI
#include   "kms_math.h"
#endif

double        v_std(
double        v
)

{
#include       "v_red.h"

  v = v_red(v);
  if (v < 0.0) v += 2.0*M_PI;

  return(v);
}


